public class literals
{
    public static void main(String a[])
    {
        int num = 0b101;
        System.out.println(num);
        int num1 = 100_00_000;
        System.out.println(num1);
        double num2 = 56;
        System.out.println(num2);
        double num3 = 12e10;
        System.out.println(num3);
        boolean bool = true;
        System.out.println(bool);
        char c = 's';
        c++;
        System.out.println(c);
    }
}